package com.liberate;

public class ReservedBookBean {
	private int accession_no;
	private String mem_id;
	private String mem_type;
	private String title;
	private String isbn;
	private String reserved_date;
	private String return_date;
	private String book_status;
	public int getAccession_no() {
		return accession_no;
	}
	public void setAccession_no(int accession_no) {
		this.accession_no = accession_no;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getMem_type() {
		return mem_type;
	}
	public void setMem_type(String mem_type) {
		this.mem_type = mem_type;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	public String getReserved_date() {
		return reserved_date;
	}
	public void setReserved_date(String reserved_date) {
		this.reserved_date = reserved_date;
	}
	public String getReturn_date() {
		return return_date;
	}
	public void setReturn_date(String return_date) {
		this.return_date = return_date;
	}
	public String getBook_status() {
		return book_status;
	}
	public void setBook_status(String book_status) {
		this.book_status = book_status;
	}
	
	

}
