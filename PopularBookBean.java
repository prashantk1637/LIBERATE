package recommendation;

public class PopularBookBean {
	private String path;
	private String title;
	private String author;
	private String isbn;
	private String edition;
	private int publication_year;
	private int no_issued_copy;
	private int no_of_remaining_copy;
	private int no_reserved_copy;
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	public String getEdition() {
		return edition;
	}
	public void setEdition(String edition) {
		this.edition = edition;
	}
	public int getPublication_year() {
		return publication_year;
	}
	public void setPublication_year(int publication_year) {
		this.publication_year = publication_year;
	}
	public int getNo_issued_copy() {
		return no_issued_copy;
	}
	public void setNo_issued_copy(int no_issued_copy) {
		this.no_issued_copy = no_issued_copy;
	}
	public int getNo_of_remaining_copy() {
		return no_of_remaining_copy;
	}
	public void setNo_of_remaining_copy(int no_of_remaining_copy) {
		this.no_of_remaining_copy = no_of_remaining_copy;
	}
	public int getNo_reserved_copy() {
		return no_reserved_copy;
	}
	public void setNo_reserved_copy(int no_reserved_copy) {
		this.no_reserved_copy = no_reserved_copy;
	}
	
	
	
	

}
